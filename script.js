const curriculumContainer = document.getElementById("curriculumContainer");

function renderCurriculum(data) {
    if (!curriculumContainer) return;

    curriculumContainer.innerHTML = "";

    data.forEach((module, index) => {

        const moduleCard = document.createElement("div");
        moduleCard.classList.add("module-card");

        moduleCard.innerHTML = `
        
            <div class="module-header">

                <h2>${module.module}</h2>

                <span class="arrow">▼</span>

            </div>

            <div class="module-content">

                ${module.topics.map(topic => `

                    <div class="topic">

                        <h3>${topic.title}</h3>

                        <ul>
                            ${topic.concepts.map(concept => `
                                <li>${concept}</li>
                            `).join("")}
                        </ul>

                        <div class="problem-section">

                        ${topic.problems.map(problem => {

            if (typeof problem === "string") {

                return `
            <div class="problem-card">

                <div class="problem-top">
                    <h4>${problem}</h4>
                </div>

            </div>
        `;
            }

            return `

        <a 
            href="${problem.link}"
            target="_blank"
            class="problem-card"
        >

            <div class="problem-top">

                <h4>${problem.title}</h4>

                <span class="
                    difficulty-badge
                    ${problem.difficulty.toLowerCase()}
                ">
                    ${problem.difficulty}
                </span>

            </div>

            <p>${problem.platform}</p>

        </a>

    `;

        }).join("")}



                        </div>

                `).join("")}

            </div>

        `;

        curriculumContainer.appendChild(moduleCard);

    });

    const headers = document.querySelectorAll(".module-header");

    headers.forEach(header => {

        header.addEventListener("click", () => {

            const content = header.nextElementSibling;

            const arrow = header.querySelector(".arrow");

            if (content.style.display === "block") {
                content.style.display = "none";
                arrow.classList.remove("rotate");
            }
            else {
                content.style.display = "block";
                arrow.classList.add("rotate");
            }

        });

    });

}

/* THEME SWITCHER */
const themeToggle = document.getElementById("themeToggle");
const savedTheme = localStorage.getItem("siteTheme") || "dark";

function applyTheme(theme) {
    document.body.classList.toggle("light-theme", theme === "light");
    if (themeToggle) {
        themeToggle.textContent = theme === "light" ? "🌙" : "☀️";
    }
    localStorage.setItem("siteTheme", theme);
}

if (themeToggle) {
    themeToggle.addEventListener("click", () => {
        const nextTheme = document.body.classList.contains("light-theme") ? "dark" : "light";
        applyTheme(nextTheme);
    });
}

applyTheme(savedTheme);

